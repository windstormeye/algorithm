package pjhubs;

public class c1ex1_1_2 {

    public static void c1ex1_1_2() {
        // 小数a除以整数 = 小数b（位数与小数a相同）
        System.out.println((1 + 2.236) / 2);
        // 整数 + 小数a = 小数b（位数与小数a相同）
        System.out.println(1 + 2 + 3 + 4.01);
        System.out.println(4.1 >= 4);
        System.out.println(1 + 2 + "3");
    }

}
